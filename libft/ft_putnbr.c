#include "libft.h"

void	ft_putnbr(int nb)
{
	unsigned	int	i;

	if (nb < 0)
	{
		ft_putchar('-');
		nb = -nb;
	}
	i = nb;
	if (i >= 10)
	{
		ft_putnbr(i / 10);
		ft_putchar(i % 10 + '0');
	}
	else
		ft_putchar(i + '0');
}
