#include "libft.h"

char	*ft_strtoupper(char *str)
{
	size_t len;
	size_t i;

	len = ft_strlen(str);
	i = 0;
	while (i < len)
	{
		if (str[i] >= 'a' && str[i] <= 'z')
			str[i] = str[i] - 'a' + 'A';
		i++;
	}
	return (str);
}
